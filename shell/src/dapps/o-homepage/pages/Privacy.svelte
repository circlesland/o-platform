  <h2 class="mt-6 text-2xl">§2 Your Information and the Blockchain</h2>
  Blockchain technology, also known as distributed ledger technology (or simply ‘DLT’), is at the core of our business. Blockchains
  are decentralized and made up of digitally recorded data in a chain of packages called ‘blocks’. The manner in which these
  blocks are linked is chronological, meaning that the data is very difficult to alter once recorded. Since the ledger may
  be distributed all over the world (across several ‘nodes’ which usually replicate the ledger) this means there is no single
  person making decisions or otherwise administering the system (such as an operator of a cloud computing system), and that
  there is no centralized place where it is located either. Accordingly, by design, a blockchains records cannot be changed
  or deleted and is said to be ‘immutable’. This may affect your ability to exercise your rights such as your right to erasure
  (‘right to be forgotten’), or your rights to object or restrict processing, of your personal data. Data on the blockchain
  cannot be erased and cannot be changed. Although smart contracts may be used to revoke certain access rights, and some
  content may be made invisible to others, it is not deleted. In certain circumstances, in order to comply with our contractual
  obligations to you (such as delivery of tokens) it will be necessary to write certain personal data, such as your CirclesAddress
  or other cryptocurrency wallet address, onto the blockchain; this is done through a smart contract and requires you to
  execute such transactions using your wallet’s private key. In most cases ultimate decisions to (i) transact on the blockchain
  using your CirclesAddress or other cryptocurrency wallet address, as well as (ii) share the public key relating to your
  Ethereum or other cryptocurrency wallet address with anyone (including us) rests with you. IF YOU WANT TO ENSURE YOUR PRIVACY
  RIGHTS ARE NOT AFFECTED IN ANY WAY, YOU SHOULD NOT TRANSACT ON BLOCKCHAINS AS CERTAIN RIGHTS MAY NOT BE FULLY AVAILABLE
